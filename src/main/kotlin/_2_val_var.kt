    // TODO : Declaring values by using val (val cannot be reassigned)
    val x = 5                       // Integer (Implicit)
    println(x)

    val x1: Int = 5                         // Integer (Explicit)
    println(x1)

    val x2 : Float = 5.3f                   // Float
    println(x2)

    val x3 : Double = 3.4                   // Double
    println(x3)

    val y : Boolean = true                  // Boolean

    val z : String = "my name is Nghiem"    // String

    // TODO : Dexlaring variables with var
    var a = "Hello world"
    a = "Bye Bye world"


}